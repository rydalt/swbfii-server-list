import { build } from "esbuild";
import { writeFileSync } from "fs";

const result = await build({
  entryPoints: ["src/frontend/client/app.ts"],
  bundle: true,
  format: "iife",
  write: false,
  minify: false,
  target: "es2020",
  tsconfig: "src/frontend/client/tsconfig.json",
});

const js = result.outputFiles[0].text;
const output = `// AUTO-GENERATED by scripts/bundle-client.mjs — do not edit.\nexport const CLIENT_JS = ${JSON.stringify(js)};\n`;
writeFileSync("src/frontend/client-bundle.ts", output);
console.log("Client bundle generated (" + js.length + " bytes).");
